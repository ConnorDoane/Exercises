public class Exercise3 {
  public static void main(String[] args) {
    double x = 4.0;
    double result = 3*(x * x) - (8*x) + 4;

    System.out.println("If x is " + x + " the result will be " + result);

    x = 2.0;
    result = 3*(x * x) - (8*x) + 4;

    System.out.println("If x is " + x + " the result will be " + result);

    x = 0.0;
    result = 3*(x * x) - (8*x) + 4;

    System.out.println("If x is " + x + " the result will be " + result);
  }
}
